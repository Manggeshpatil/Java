class switchvowel
{
	public static void main(String[] args) 
	{
		char ch ='i';
	switch (ch)
	{
		case 'a':
		case 'e':
		case 'i':
		case 'o':
		case 'u':
		
		
		{
			System.out.println("it is vowel");
	}
	break;
	default:
			{
				System.out.println("28 days");
			}
		}
	}
}